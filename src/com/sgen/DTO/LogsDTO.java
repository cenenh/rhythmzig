package com.sgen.DTO;

import java.io.Serializable;
import java.util.Date;

public class LogsDTO implements Serializable{
	private int enterlogIdx;
	private String userCode;
	private String deviceCode;
	private int deviceStatus;
	private java.sql.Timestamp enterTime;
	private String userName;
	
	

	
	public int getEnterlogIdx() {
		return enterlogIdx;
	}
	public void setEnterlogIdx(int enterlogIdx) {
		this.enterlogIdx = enterlogIdx;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getDeviceCode() {
		return deviceCode;
	}
	public void setDeviceCode(String deviceCode) {
		this.deviceCode = deviceCode;
	}
	public int getDeviceStatus() {
		return deviceStatus;
	}
	public void setDeviceStatus(int deviceStatus) {
		this.deviceStatus = deviceStatus;
	}
	public java.sql.Timestamp getEnterTime() {
		return enterTime;
	}
	public void setEnterTime(java.sql.Timestamp enterTime) {
		this.enterTime = enterTime;
	}


}
